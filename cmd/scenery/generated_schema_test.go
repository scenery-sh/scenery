func copyModelDSLWebFixture(t *testing.T, root string) {
	t.Helper()
	webRoot := filepath.Join(repoRootForTest(t), "testdata", "apps", "model-dsl", "web")
	if err := filepath.WalkDir(webRoot, func(path string, entry os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if entry.IsDir() {
			if entry.Name() == "node_modules" {
				return filepath.SkipDir
			}
			return nil
		}
		relativePath, err := filepath.Rel(webRoot, path)
		if err != nil {
			return err
		}
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		writeTestAppFile(t, root, filepath.Join("web", relativePath), string(data))
		return nil
	}); err != nil {
		t.Fatalf("copy web fixture: %v", err)
	}
}

func writeExistingTableDSLAppFixture(t *testing.T) string {
	t.Helper()
	root := t.TempDir()
	sourceRoot := filepath.Join(repoRootForTest(t), "testdata", "apps", "existing-table-dsl")
	if err := filepath.WalkDir(sourceRoot, func(path string, entry os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if entry.IsDir() {
			switch entry.Name() {
			case ".scenery", "node_modules":
				return filepath.SkipDir
			default:
				return nil
			}
		}
		relativePath, err := filepath.Rel(sourceRoot, path)
		if err != nil {
			return err
		}
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		text := strings.ReplaceAll(string(data), "../../..", repoRootForTest(t))
		writeTestAppFile(t, root, relativePath, text)
		return nil
	}); err != nil {
		t.Fatalf("copy existing-table fixture: %v", err)
	}
	return root
}

