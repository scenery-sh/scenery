func readBackendStateForInstall(path string) (neonselfhost.BackendState, bool, error) {
	state, ok, err := neonselfhost.ReadBackendState(path)
	if err != nil {
		return neonselfhost.BackendState{}, false, fmt.Errorf("read Neon backend state: %w", err)
	}
	return state, ok, nil
}
