func writeGeneratedNeonFiles(state neonCellState) error {
	if err := writeGeneratedNeonCompose(state); err != nil {
		return err
	}
	if err := writeGeneratedPageserverConfig(filepath.Join(state.Root, "pageserver_config", "pageserver.toml")); err != nil {
		return err
	}
	if err := writeGeneratedPageserverIdentity(filepath.Join(state.Root, "pageserver_config", "identity.toml")); err != nil {
		return err
	}
	if err := writeGeneratedComputeTemplate(filepath.Join(state.Root, "compute_templates")); err != nil {
		return err
	}
	return neonselfhost.WithBackendStateLock(state.Root, func() error {
		if _, ok, err := readBackendStateForInstall(filepath.Join(state.Root, "backend.json")); err != nil {
			return err
		} else if !ok {
			backend := neonselfhost.NewBackendState()
			if err := neonselfhost.WriteBackendState(filepath.Join(state.Root, "backend.json"), backend); err != nil {
				return err
			}
		}
		return nil
	})
}

