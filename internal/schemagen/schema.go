package schemagen

import (
	"bytes"
	"fmt"
	"path/filepath"
	"sort"
	"strings"

	"scenery.sh/internal/model"
)

type ServiceSchema struct {
	Service       string
	SourcePath    string
	GeneratedPath string
	HCL           string
	Entities      []string
}

type Drift struct {
	Service       string
	SourcePath    string
	GeneratedPath string
	Message       string
}

func Build(appRoot string, app *model.App) ([]ServiceSchema, error) {
	if app == nil || len(app.Entities) == 0 {
		return nil, nil
	}
	byService := make(map[string][]*model.Entity)
	for _, entity := range app.Entities {
		if entity == nil {
			continue
		}
		service := model.EntityService(entity)
		byService[service] = append(byService[service], entity)
	}
	services := make([]string, 0, len(byService))
	for service := range byService {
		services = append(services, service)
	}
	sort.Strings(services)
	out := make([]ServiceSchema, 0, len(services))
	for _, service := range services {
		entities := byService[service]
		sort.Slice(entities, func(i, j int) bool {
			if entities[i].Table != entities[j].Table {
				return entities[i].Table < entities[j].Table
			}
			return entities[i].Name < entities[j].Name
		})
		names := make([]string, 0, len(entities))
		for _, entity := range entities {
			names = append(names, entity.Name)
		}
		out = append(out, ServiceSchema{
			Service:       service,
			SourcePath:    filepath.ToSlash(filepath.Join(service, "db", "schema.hcl")),
			GeneratedPath: filepath.ToSlash(filepath.Join(".scenery", "gen", "db", service, "schema.hcl")),
			HCL:           renderServiceHCL(entities),
			Entities:      names,
		})
	}
	return out, nil
}

func Diff(appRoot string, schemas []ServiceSchema, readFile func(string) ([]byte, error), pathExists func(string) bool) ([]Drift, error) {
	var drift []Drift
	for _, schema := range schemas {
		sourcePath := filepath.Join(appRoot, filepath.FromSlash(schema.SourcePath))
		if !pathExists(sourcePath) {
			drift = append(drift, Drift{
				Service:       schema.Service,
				SourcePath:    schema.SourcePath,
				GeneratedPath: schema.GeneratedPath,
				Message:       fmt.Sprintf("missing source schema %s for generated model schema %s", schema.SourcePath, schema.GeneratedPath),
			})
			continue
		}
		data, err := readFile(sourcePath)
		if err != nil {
			return nil, err
		}
		if normalizeHCL(string(data)) != normalizeHCL(schema.HCL) {
			drift = append(drift, Drift{
				Service:       schema.Service,
				SourcePath:    schema.SourcePath,
				GeneratedPath: schema.GeneratedPath,
				Message:       fmt.Sprintf("source schema %s differs from generated model schema %s", schema.SourcePath, schema.GeneratedPath),
			})
		}
	}
	return drift, nil
}

func renderServiceHCL(entities []*model.Entity) string {
	var b bytes.Buffer
	b.WriteString("// Code generated by scenery generate data; DO NOT EDIT.\n\n")
	schemas := map[string]bool{}
	for _, entity := range entities {
		schemas[model.EntityDatabaseSchema(entity)] = true
	}
	for _, schema := range sortedMapKeys(schemas) {
		fmt.Fprintf(&b, "schema %q {}\n\n", schema)
	}
	for _, entity := range entities {
		schema := model.EntityDatabaseSchema(entity)
		for _, field := range entity.Fields {
			if len(field.EnumValues) == 0 || field.Kind == model.EntityFieldComputed {
				continue
			}
			fmt.Fprintf(&b, "enum %q %q {\n", schema, enumName(entity, field))
			fmt.Fprintf(&b, "  schema = schema.%s\n", hclRef(schema))
			b.WriteString("  values = [")
			for i, value := range field.EnumValues {
				if i > 0 {
					b.WriteString(", ")
				}
				fmt.Fprintf(&b, "%q", value)
			}
			b.WriteString("]\n")
			b.WriteString("}\n\n")
		}
		fmt.Fprintf(&b, "table %q %q {\n", schema, entity.Table)
		fmt.Fprintf(&b, "  schema = schema.%s\n\n", hclRef(schema))
		for _, field := range entity.Fields {
			if field.Kind == model.EntityFieldComputed {
				continue
			}
			fmt.Fprintf(&b, "  column %q {\n", field.Column)
			fmt.Fprintf(&b, "    null = %t\n", columnNullable(field.TypeExpr))
			fmt.Fprintf(&b, "    type = %s\n", columnType(entity, field))
			b.WriteString("  }\n\n")
		}
		if pk := primaryKeyField(entity); pk != nil {
			b.WriteString("  primary_key {\n")
			fmt.Fprintf(&b, "    columns = [column.%s]\n", hclRef(pk.Column))
			b.WriteString("  }\n")
		}
		b.WriteString("}\n\n")
	}
	return b.String()
}

func primaryKeyField(entity *model.Entity) *model.EntityField {
	for i := range entity.Fields {
		field := &entity.Fields[i]
		if field.Kind != model.EntityFieldComputed && strings.EqualFold(field.Name, "id") {
			return field
		}
	}
	return nil
}

func sortedMapKeys(values map[string]bool) []string {
	keys := make([]string, 0, len(values))
	for key := range values {
		keys = append(keys, key)
	}
	sort.Strings(keys)
	return keys
}

func columnNullable(typeExpr string) bool {
	return strings.HasPrefix(strings.TrimSpace(typeExpr), "*")
}

func columnType(entity *model.Entity, field model.EntityField) string {
	if len(field.EnumValues) > 0 {
		return "enum." + hclRef(model.EntityDatabaseSchema(entity)) + "." + hclRef(enumName(entity, field))
	}
	t := strings.TrimPrefix(strings.TrimSpace(field.TypeExpr), "*")
	switch t {
	case "string":
		return "text"
	case "bool":
		return "bool"
	case "int", "int32", "uint", "uint32":
		return "integer"
	case "int64", "uint64":
		return "bigint"
	case "float32", "float64":
		return "double_precision"
	case "time.Time":
		return "timestamptz"
	case "[]byte":
		return "bytea"
	default:
		if strings.HasSuffix(t, ".UUID") {
			return "uuid"
		}
		return "text"
	}
}

func enumName(entity *model.Entity, field model.EntityField) string {
	return safeIdent(entity.Table + "_" + field.Column)
}

func hclRef(value string) string {
	if value == safeIdent(value) {
		return value
	}
	return fmt.Sprintf("%q", value)
}

func safeIdent(value string) string {
	value = strings.ToLower(strings.TrimSpace(value))
	var b strings.Builder
	lastUnderscore := false
	for _, r := range value {
		ok := (r >= 'a' && r <= 'z') || (r >= '0' && r <= '9')
		if ok {
			b.WriteRune(r)
			lastUnderscore = false
			continue
		}
		if !lastUnderscore {
			b.WriteByte('_')
			lastUnderscore = true
		}
	}
	out := strings.Trim(b.String(), "_")
	if out == "" {
		return "value"
	}
	if out[0] >= '0' && out[0] <= '9' {
		return "n_" + out
	}
	return out
}

func normalizeHCL(value string) string {
	value = strings.ReplaceAll(value, "\r\n", "\n")
	lines := strings.Split(value, "\n")
	for i, line := range lines {
		lines[i] = strings.TrimRight(line, " \t")
	}
	return strings.TrimSpace(strings.Join(lines, "\n"))
}
