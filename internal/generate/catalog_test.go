func TestBinaryOwnedUICatalogContainsComposableQueryTable(t *testing.T) {
	root := t.TempDir()
	files, err := renderUICatalog("", root)
	if err != nil {
		t.Fatal(err)
	}
	content := map[string]string{}
	for _, file := range files {
		path := filepath.ToSlash(strings.TrimPrefix(file.Path, root+string(filepath.Separator)))
		content[path] = string(file.Bytes)
		if !trustedGeneratedArtifact("", path, file.Bytes) {
			t.Errorf("catalog %s has no trusted generated marker", path)
		}
		if filepath.Ext(path) == ".css" {
			t.Errorf("catalog materialized obsolete CSS file %s", path)
		}
	}
	for path, fragment := range map[string]string{
		"components/DataTable.tsx":      "export function DataTable",
		"components/PageLayout.tsx":     "defineContentPageSlots",
		"components/QueryTable.tsx":     "export function QueryTable",
		"components/request-state.ts":   "export type RequestState",
		"components/SplitPage.tsx":      "defineSplitPageSlots",
		"components/SideNavigation.tsx": "export function SideNavigation",
		"components/Theme.tsx":          "stylex.props(tTheme",
		"components/TopBar.tsx":         "export function TopBar",
		"global.d.ts":                   "Code generated by Scenery",
		"index.ts":                      `from "./components/QueryTable.js"`,
		"package.json":                  `"name": "@scenery/ui"`,
		"tokens.stylex.ts":              `export const tTheme = stylex.createTheme`,
	} {
		if !strings.Contains(content[path], fragment) {
			t.Errorf("catalog %s missing %q", path, fragment)
		}
	}
	queryTable := content["components/QueryTable.tsx"]
	for _, fragment := range []string{
		`@astryxdesign/core/DateTimeInput`,
		`@astryxdesign/core/Pagination`,
		`@astryxdesign/core/Resizable`,
		`@astryxdesign/core/Selector`,
		`@tanstack/react-query`,
		`requestStateFromQuery<{`,
		`<DataTable`,
		`<QueryState`,
		`paginated = true`,
		`paginated && result.kind === "result"`,
		`columns.filter((column) => !column.hidden)`,
		`columns.filter((column) => column.export !== false)`,
		`const label = statusMap[value]?.label`,
		`export interface TablePageGroup`,
		`detailPanel?: ComponentType<TablePageDetailPanelProps<Row>>`,
		`<ResizeHandle`,
	} {
		if !strings.Contains(queryTable, fragment) {
			t.Errorf("QueryTable missing %q", fragment)
		}
	}
	dataTable := content["components/DataTable.tsx"]
	for _, fragment := range []string{
		"export type DataTableSection",
		`@astryxdesign/core/Table`,
		"useTableGroupedRows",
		"useTableRowIndex",
		"useTableSortable",
		"numbered?: boolean",
	} {
		if !strings.Contains(dataTable, fragment) {
			t.Errorf("DataTable missing %q", fragment)
		}
	}
	statTile := content["components/StatTile.tsx"]
	for _, fragment := range []string{
		`@astryxdesign/core/ClickableCard`,
		`@astryxdesign/core/Grid`,
		`@astryxdesign/core/SelectableCard`,
		`<ClickableCard`,
		`<Grid`,
		`<SelectableCard`,
		`columns={{ minWidth: 180, max: columns, repeat: "fit" }}`,
		`!active && styles.tileInteractive`,
	} {
		if !strings.Contains(statTile, fragment) {
			t.Errorf("StatTile missing %q", fragment)
		}
	}
	formDialog := content["components/FormDialog.tsx"]
	for _, fragment := range []string{
		`@astryxdesign/core/Banner`,
		`@astryxdesign/core/FormLayout`,
		`<FormLayout`,
		`<Banner status="error"`,
	} {
		if !strings.Contains(formDialog, fragment) {
			t.Errorf("FormDialog missing %q", fragment)
		}
	}
	if strings.Contains(formDialog, "export function Field") {
		t.Error("FormDialog still contains a catalog-owned Field wrapper")
	}
	filterToolbar := content["components/FilterToolbar.tsx"]
	for _, fragment := range []string{
		`@astryxdesign/core/Toolbar`,
		`<Toolbar`,
		`startContent={`,
		`endContent={`,
		`alignItems: "flex-end"`,
		`paddingBlock: spacingVars["--spacing-1"]`,
	} {
		if !strings.Contains(filterToolbar, fragment) {
			t.Errorf("FilterToolbar missing %q", fragment)
		}
	}
	for _, fragment := range []string{"styles.toolbar", "styles.trailing"} {
		if strings.Contains(filterToolbar, fragment) {
			t.Errorf("FilterToolbar still owns outer toolbar layout through %q", fragment)
		}
	}
	for _, fragment := range []string{
		`from "@astryxdesign/core/Field"`,
		`from "@astryxdesign/core/FormLayout"`,
		`from "@astryxdesign/core/Banner"`,
	} {
		if !strings.Contains(content["index.ts"], fragment) {
			t.Errorf("catalog index missing %q", fragment)
		}
	}
	for path, source := range content {
		if path == "components/FilterPills.tsx" {
			continue
		}
		for _, rawControl := range []string{"<button", "<input", "<select", "<textarea", "<table"} {
			if strings.Contains(source, rawControl) {
				t.Errorf("catalog %s contains raw interactive element %q", path, rawControl)
			}
		}
	}
	for path := range content {
		if strings.HasPrefix(path, "pages/") {
			t.Errorf("catalog still materializes obsolete page surface %s", path)
		}
	}
}

func TestRenderUICatalogFromConfiguredDirectory(t *testing.T) {
	appRoot := t.TempDir()
	catalog := filepath.Join(appRoot, "ui-src")
	for path, contents := range map[string]string{
		"package.json":          `{"name":"@scenery/ui"}`,
		"index.ts":              `export * from "./components/Button.js";`,
		"tokens.stylex.ts":      `export const t = {};`,
		"components/Button.tsx": "export function Button() { return null; }",
		"pages/Obsolete.css":    ":root { color: red; }",
		"AGENTS.md":             "not a catalog file",
		"embed.go":              "package ui",
	} {
		full := filepath.Join(catalog, filepath.FromSlash(path))
		if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(full, []byte(contents), 0o644); err != nil {
			t.Fatal(err)
		}
	}
	config := `{"name":"catalogapp","envs":{"local":{"default":true,"ui_catalog":"ui-src"}}}`
	if err := os.WriteFile(filepath.Join(appRoot, ".scenery.json"), []byte(config), 0o644); err != nil {
		t.Fatal(err)
	}

	out := t.TempDir()
	files, err := renderUICatalog(appRoot, out)
	if err != nil {
		t.Fatal(err)
	}
	content := map[string]string{}
	for _, file := range files {
		path := filepath.ToSlash(strings.TrimPrefix(file.Path, out+string(filepath.Separator)))
		content[path] = string(file.Bytes)
	}
	if got := content["components/Button.tsx"]; !strings.Contains(got, "export function Button") || !strings.HasPrefix(got, "// Code generated by Scenery.") {
		t.Errorf("Button.tsx = %q, want live content with generated marker", got)
	}
	if got := content["package.json"]; !strings.Contains(got, `"_generated"`) {
		t.Errorf("package.json = %q, want _generated marker", got)
	}
	if got := content["tokens.stylex.ts"]; !strings.Contains(got, "export const t") || !strings.HasPrefix(got, "// Code generated by Scenery.") {
		t.Errorf("tokens.stylex.ts = %q, want live content with generated marker", got)
	}
	for _, excluded := range []string{"AGENTS.md", "embed.go", "pages/Obsolete.css"} {
		if _, ok := content[excluded]; ok {
			t.Errorf("catalog materialized repo file %s", excluded)
		}
	}
}

func TestIncludeStaleUICatalogFilesRetiresOnlyGeneratedFiles(t *testing.T) {
	root := t.TempDir()
	catalogRoot := filepath.Join(root, ".scenery", "gen", "typescript", "public_api", "react", "scenery-ui")
	for path, contents := range map[string]string{
		"components/Current.tsx": "// Code generated by Scenery. DO NOT EDIT.\nexport {};\n",
		"components/Stale.tsx":   "// Code generated by Scenery. DO NOT EDIT.\nexport {};\n",
		"components/Owned.tsx":   "export {};\n",
	} {
		full := filepath.Join(catalogRoot, filepath.FromSlash(path))
		if err := os.MkdirAll(filepath.Dir(full), 0o755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(full, []byte(contents), 0o644); err != nil {
			t.Fatal(err)
		}
	}
	result := &Result{Root: root}
	target := Resource{
		Name: "public_api",
		Spec: map[string]any{
			"materialization": "cache",
			"react":           map[string]any{},
		},
	}
	files := []generatedFile{{
		Path:  filepath.Join(catalogRoot, "components", "Current.tsx"),
		Bytes: []byte("// Code generated by Scenery. DO NOT EDIT.\nexport {};\n"),
	}}
	files, err := includeStaleUICatalogFiles(result, []Resource{target}, files)
	if err != nil {
		t.Fatal(err)
	}
	var removed []string
	for _, file := range files {
		if file.Remove {
			removed = append(removed, filepath.Base(file.Path))
		}
	}
	if len(removed) != 1 || removed[0] != "Stale.tsx" {
		t.Fatalf("removed = %v, want [Stale.tsx]", removed)
	}
}

func TestRenderUICatalogMissingConfiguredDirectoryFallsBackToEmbed(t *testing.T) {
	appRoot := t.TempDir()
	config := `{"name":"catalogapp","envs":{"local":{"default":true,"ui_catalog":"no-such-dir"}}}`
	if err := os.WriteFile(filepath.Join(appRoot, ".scenery.json"), []byte(config), 0o644); err != nil {
		t.Fatal(err)
	}
	files, err := renderUICatalog(appRoot, t.TempDir())
	if err != nil {
		t.Fatal(err)
	}
	found := false
	for _, file := range files {
		if strings.HasSuffix(filepath.ToSlash(file.Path), "components/DataTable.tsx") {
			found = true
		}
	}
	if !found {
		t.Fatal("embedded catalog fallback missing components/DataTable.tsx")
	}
}

func TestRenderUICatalogImplausibleConfiguredDirectoryFails(t *testing.T) {
	appRoot := t.TempDir()
	if err := os.MkdirAll(filepath.Join(appRoot, "empty"), 0o755); err != nil {
		t.Fatal(err)
	}
	config := `{"name":"catalogapp","envs":{"local":{"default":true,"ui_catalog":"empty"}}}`
	if err := os.WriteFile(filepath.Join(appRoot, ".scenery.json"), []byte(config), 0o644); err != nil {
		t.Fatal(err)
	}
	if _, err := renderUICatalog(appRoot, t.TempDir()); err == nil || !strings.Contains(err.Error(), "not a UI catalog root") {
		t.Fatalf("error = %v, want catalog-root rejection", err)
	}
}
