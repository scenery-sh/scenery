package generate

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestGeneratedGoVerificationOverlayMasksRetiredDeclarations(t *testing.T) {
	path := filepath.Join(t.TempDir(), "obsolete.gen.go")
	if err := os.WriteFile(path, []byte("// Code generated by Scenery. DO NOT EDIT.\n\npackage sample\n\nvar Broken = missingSymbol\n"), 0o644); err != nil {
		t.Fatal(err)
	}
	overlay, err := generatedGoVerificationOverlay([]generatedFile{{Path: path, Remove: true}})
	if err != nil {
		t.Fatal(err)
	}
	absolute, _ := filepath.Abs(path)
	if got := string(overlay[absolute]); !strings.Contains(got, "package sample") || strings.Contains(got, "missingSymbol") {
		t.Fatalf("retired Go overlay = %q", got)
	}
}
