package generate

import (
	"fmt"
	"sort"
	"strconv"
	"strings"
)

type reactWorkspacePage struct {
	workspace                    Resource
	statsBinding, statsOperation Resource
	statsRecord                  Resource
}

func selectedReactWorkspacePages(resources, bindings []Resource) []reactWorkspacePage {
	byAddress := resourcesByAddress(&Manifest{Resources: resources})
	selectedBindings := map[string]Resource{}
	for _, binding := range bindings {
		selectedBindings[binding.Address] = binding
	}
	var pages []reactWorkspacePage
	for _, workspace := range resources {
		if workspace.Kind != "scenery.workspace-page" || workspace.Origin.Kind == "expanded" {
			continue
		}
		page := reactWorkspacePage{workspace: workspace}
		if stats := orderedChildren(workspace.Spec, "stats"); len(stats) > 0 {
			page.statsBinding = selectedBindings[resolveResourceRef(workspace, refString(stats[0]["source"]), "binding")]
			if page.statsBinding.Address != "" {
				page.statsOperation = byAddress[resolveResourceRef(page.statsBinding, refString(page.statsBinding.Spec["operation"]), "operation")]
				results := namedChildren(page.statsOperation.Spec, "result")
				if len(results) == 1 {
					page.statsRecord = byAddress[resolveResourceRef(page.statsOperation, refString(results[0]["type"]), "record")]
				}
			}
		}
		pages = append(pages, page)
	}
	sort.Slice(pages, func(i, j int) bool { return pages[i].workspace.Address < pages[j].workspace.Address })
	return pages
}

func renderReactWorkspacePage(result *Result, target Resource, reactRoot string, page reactWorkspacePage, bindings []Resource) (string, error) {
	resources := resourcesByAddress(result.Manifest)
	statsChildren := orderedChildren(page.workspace.Spec, "stats")
	statsTiles := []map[string]any(nil)
	if len(statsChildren) > 0 {
		statsTiles = orderedChildren(statsChildren[0], "tile")
	}
	statsIcons := false
	for _, tile := range statsTiles {
		statsIcons = statsIcons || stringValue(tile["icon"]) != ""
	}
	if len(statsChildren) > 0 && page.statsBinding.Address == "" {
		return "", fmt.Errorf("workspace_page %s stats binding is not included by TypeScript client %s", page.workspace.Address, target.Address)
	}
	var actionAlias string
	if actions := orderedChildren(page.workspace.Spec, "actions"); len(actions) > 0 {
		actionAlias = "SceneryWorkspaceActions"
	}

	var b strings.Builder
	b.WriteString("// Code generated by Scenery. DO NOT EDIT.\n")
	if len(statsChildren) > 0 {
		b.WriteString("import { useQuery } from \"@tanstack/react-query\";\n")
	}
	b.WriteString("import { useCallback, useEffect, useMemo, useState } from \"react\";\n")
	fmt.Fprintf(&b, "import { %sClient, url } from \"../index.js\";\n", goName(target.Name))
	if page.statsRecord.Address != "" {
		fmt.Fprintf(&b, "import type { %s } from \"../index.js\";\n", goName(page.statsRecord.Name))
	}
	b.WriteString("import { WorkspacePage as SceneryWorkspacePage")
	if len(statsChildren) > 0 {
		b.WriteString(", QueryState, queryStateProps, requestStateFromQuery")
		if len(statsTiles) > 0 {
			b.WriteString(", StatGrid, StatTile")
			if statsIcons {
				b.WriteString(", Icon")
			}
		}
	}
	b.WriteString(" } from \"./scenery-ui/index.js\";\n")
	if actions := orderedChildren(page.workspace.Spec, "actions"); len(actions) > 0 {
		component := resources[resolveResourceRef(page.workspace, refString(actions[0]["component"]), "react_component")]
		module, err := reactComponentImport(result, reactRoot, component)
		if err != nil {
			return "", err
		}
		fmt.Fprintf(&b, "import { %s as %s } from %s;\n", stringValue(component.Spec["export"]), actionAlias, strconv.Quote(module))
	}
	tabs := orderedChildren(page.workspace.Spec, "tab")
	for index, tab := range tabs {
		pageRef := refString(tab["page"])
		if pageRef == "" {
			continue
		}
		child := resources[resolveResourceRef(page.workspace, pageRef, "table_page")]
		if child.Address == "" {
			return "", fmt.Errorf("workspace_page %s tab %s is unavailable", page.workspace.Address, stringValue(tab["name"]))
		}
		fmt.Fprintf(&b, "import { %sPage as SceneryWorkspaceTab%d } from %s;\n", goName(child.Name), index+1, strconv.Quote("./"+child.Name+".generated.js"))
	}
	b.WriteString("\n")
	if len(statsChildren) > 0 {
		fmt.Fprintf(&b, "const statsQueryKey = [\"scenery\", \"workspace_page\", %s, \"stats\"] as const;\n\n", strconv.Quote(page.workspace.Address))
		if len(statsTiles) > 0 {
			b.WriteString("function formatStatValue(value: unknown, appearance: \"plain\" | \"money\" | \"count\" | \"percent\"): string {\n")
			b.WriteString("  if (appearance === \"money\") return new Intl.NumberFormat(\"en-US\", { style: \"currency\", currency: \"USD\", maximumFractionDigits: 0 }).format(Number(value) || 0);\n")
			b.WriteString("  if (appearance === \"count\") return new Intl.NumberFormat(\"en-US\", { maximumFractionDigits: 0 }).format(Number(value) || 0);\n")
			b.WriteString("  if (appearance === \"percent\") return `${new Intl.NumberFormat(\"en-US\", { maximumFractionDigits: 2 }).format(Number(value) || 0)}%`;\n")
			b.WriteString("  return String(value ?? \"\");\n}\n\n")
		}
	}
	writeReactPageOpen(&b, goName(page.workspace.Name), goName(target.Name))
	var pageTabs []map[string]any
	for _, tab := range tabs {
		if refString(tab["page"]) != "" {
			pageTabs = append(pageTabs, tab)
		}
	}
	if len(pageTabs) == 0 {
		return "", fmt.Errorf("workspace_page %s has no page-backed tabs", page.workspace.Address)
	}
	firstName := stringValue(pageTabs[0]["name"])
	validNames := make([]string, 0, len(tabs))
	for _, tab := range pageTabs {
		validNames = append(validNames, strconv.Quote(stringValue(tab["name"])))
	}
	fmt.Fprintf(&b, "  const tabNames = useMemo(() => [%s] as const, []);\n", strings.Join(validNames, ", "))
	fmt.Fprintf(&b, "  const [activeTab, setActiveTab] = useState<string>(() => { if (typeof globalThis.location === \"undefined\") return %s; const value = new URLSearchParams(globalThis.location.search).get(\"tab\"); return value && (tabNames as readonly string[]).includes(value) ? value : %s; });\n", strconv.Quote(firstName), strconv.Quote(firstName))
	b.WriteString("  useEffect(() => { if (typeof globalThis.addEventListener !== \"function\") return; const sync = () => { const value = new URLSearchParams(globalThis.location.search).get(\"tab\"); setActiveTab(value && (tabNames as readonly string[]).includes(value) ? value : tabNames[0]); }; globalThis.addEventListener(\"popstate\", sync); return () => globalThis.removeEventListener(\"popstate\", sync); }, [tabNames]);\n")
	b.WriteString("  const onTabChange = useCallback((name: string) => { setActiveTab(name); if (typeof globalThis.location === \"undefined\") return; const next = new URL(globalThis.location.href); if (name === tabNames[0]) next.searchParams.delete(\"tab\"); else next.searchParams.set(\"tab\", name); globalThis.history.pushState({}, \"\", next); }, [tabNames]);\n")
	if len(statsChildren) > 0 {
		method := reactOperationClientMethod(page.statsOperation, page.statsBinding, bindings)
		resultType := goName(page.statsRecord.Name)
		b.WriteString("  const statsQuery = useQuery({ queryKey: statsQueryKey, queryFn: async () => {\n")
		fmt.Fprintf(&b, "    const outcome = await client.%s({});\n", method)
		fmt.Fprintf(&b, "    if (outcome.kind === \"result\") return { kind: \"result\", value: outcome.value } as const satisfies { readonly kind: \"result\"; readonly value: %s };\n", resultType)
		b.WriteString("    return { kind: \"error\", name: outcome.name, problem: outcome.problem } as const;\n  } });\n")
		fmt.Fprintf(&b, "  const statsState = requestStateFromQuery<{ readonly value: %s }>(statsQuery);\n", resultType)
	}
	fmt.Fprintf(&b, "  return <SceneryWorkspacePage title=%s presentation=%s activeTab={activeTab} onTabChange={onTabChange}", jsxStringExpression(stringValue(page.workspace.Spec["title"])), jsxStringExpression(defaultString(stringValue(page.workspace.Spec["presentation"]), "tabs")))
	if actionAlias != "" {
		fmt.Fprintf(&b, " actions={<%s />}", actionAlias)
	}
	if len(statsChildren) > 0 {
		b.WriteString(" stats={<QueryState {...queryStateProps(statsState, \"statistics\")} retry={() => void statsQuery.refetch()}>")
		if len(statsTiles) > 0 {
			b.WriteString("{statsState.kind === \"result\" ? <StatGrid columns={")
			fmt.Fprintf(&b, "%d", len(statsTiles))
			b.WriteString("}>")
			for _, tile := range statsTiles {
				appearance := defaultString(stringValue(tile["appearance"]), "plain")
				fmt.Fprintf(&b, "<StatTile label=%s value={formatStatValue(statsState.value.%s, %s)}", jsxStringExpression(stringValue(tile["label"])), tsName(stringValue(tile["name"])), strconv.Quote(appearance))
				if sub := stringValue(tile["sub"]); sub != "" {
					subAppearance := defaultString(stringValue(tile["sub_appearance"]), "plain")
					suffix := ""
					if label := strings.TrimSpace(stringValue(tile["sub_label"])); label != "" {
						suffix = " + " + strconv.Quote(" "+label)
					}
					fmt.Fprintf(&b, " sub={formatStatValue(statsState.value.%s, %s)%s}", tsName(sub), strconv.Quote(subAppearance), suffix)
				}
				if icon := stringValue(tile["icon"]); icon != "" {
					fmt.Fprintf(&b, " icon={<Icon icon=%s size=\"sm\" />}", strconv.Quote(icon))
				}
				b.WriteString(" />")
			}
			b.WriteString("</StatGrid> : null}")
		} else {
			b.WriteString("{null}")
		}
		b.WriteString("</QueryState>}")
	}
	b.WriteString(" tabs={[\n")
	for index, tab := range tabs {
		fmt.Fprintf(&b, "    { name: %s, label: %s", strconv.Quote(stringValue(tab["name"])), strconv.Quote(stringValue(tab["label"])))
		if description := stringValue(tab["description"]); description != "" {
			fmt.Fprintf(&b, ", description: %s", strconv.Quote(description))
		}
		if group := stringValue(tab["group"]); group != "" {
			fmt.Fprintf(&b, ", group: %s", strconv.Quote(group))
		}
		if count := stringValue(tab["count"]); count != "" {
			fmt.Fprintf(&b, ", count: statsState.kind === \"result\" ? statsState.value.%s : undefined", tsName(count))
		}
		if available := stringValue(tab["available"]); available != "" {
			fmt.Fprintf(&b, ", available: statsState.kind === \"result\" ? statsState.value.%s : undefined", tsName(available))
		}
		if reason := stringValue(tab["unavailable_reason"]); reason != "" {
			fmt.Fprintf(&b, ", unavailableReason: %s", strconv.Quote(reason))
		}
		if destination := stringValue(tab["destination"]); destination != "" {
			fmt.Fprintf(&b, ", destination: %s", strconv.Quote(destination))
		} else if pageRef := refString(tab["page"]); pageRef != "" {
			child := resources[resolveResourceRef(page.workspace, pageRef, "table_page")]
			if child.Kind == "scenery.content-page" && refString(child.Spec["source"]) == "" {
				fmt.Fprintf(&b, ", content: <SceneryWorkspaceTab%d />", index+1)
			} else {
				fmt.Fprintf(&b, ", content: <SceneryWorkspaceTab%d client={client} />", index+1)
			}
		}
		b.WriteString(" },\n")
	}
	b.WriteString("  ]} />;\n}\n")
	return b.String(), nil
}
