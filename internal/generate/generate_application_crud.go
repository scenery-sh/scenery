func renderProviderCRUDAdapterSource(contractRevision, packageIdentity, packageABI, contractImport, packageName string, service Resource, operations, bindings, resources []Resource, covered []string, providerABIs map[string]string) ([]byte, error) {
	providerOperations := providerCRUDOperations(operations)
	crudSpec, databaseName, err := providerCRUDRuntimeSpec(resources, providerOperations)
	if err != nil {
		return nil, err
	}
	serviceName := goName(service.Name)
	var b strings.Builder
	b.WriteString("// Code generated by Scenery. DO NOT EDIT.\npackage " + packageName + "\n\n")
	b.WriteString("import (\n\t\"context\"\n\t\"errors\"\n\t\"fmt\"\n\t\"net/http\"\n\tscenery \"scenery.sh\"\n\t\"scenery.sh/datasource\"\n\tscenerydb \"scenery.sh/db\"\n\tsceneryruntime \"scenery.sh/runtime\"\n")
	fmt.Fprintf(&b, "\tcontract %q\n", contractImport)
	b.WriteString(")\n\n")
	fmt.Fprintf(&b, "const ContractRevision = %q\nconst PackageIdentity = %q\nconst PackageContractABIRevision = %q\n\n", contractRevision, packageIdentity, packageABI)
	b.WriteString("type serviceImplementation interface {\n")
	for _, operation := range providerOperations {
		method := operationHandlerMethod(operation)
		fmt.Fprintf(&b, "\t%s(context.Context, contract.%sInput) (contract.%sOutcome, error)\n", method, goName(operation.Name), goName(operation.Name))
	}
	b.WriteString("}\n\ntype providerCRUDService struct { database datasource.SQL }\n\nvar service serviceImplementation\n\n")
	if err := renderDurableDispatchOptionHelpers(&b, providerOperations, resources); err != nil {
		return nil, err
	}
	for _, operation := range providerOperations {
		handler, _ := operation.Spec["handler"].(map[string]any)
		action := stringValue(handler["action"])
		results := namedChildren(operation.Spec, "result")
		if len(results) != 1 {
			return nil, fmt.Errorf("provider CRUD operation %s requires exactly one result", operation.Address)
		}
		result := results[0]
		operationName := goName(operation.Name)
		fmt.Fprintf(&b, "func (value providerCRUDService) %s(ctx context.Context, input contract.%sInput) (contract.%sOutcome, error) {\n", operationHandlerMethod(operation), operationName, operationName)
		fmt.Fprintf(&b, "\traw, err := scenery.MarshalContractValue(input, %q)\n\tif err != nil { return nil, err }\n", goWireTypeExpression(operation.Spec["input"]))
		fmt.Fprintf(&b, "\tresult, err := datasource.InvokeCRUD(ctx, value.database, %s, %q, raw)\n", crudSpec, action)
		for _, failure := range namedChildren(operation.Spec, "error") {
			switch stringValue(failure["name"]) {
			case "not_found":
				fmt.Fprintf(&b, "\tif errors.Is(err, datasource.ErrCRUDNotFound) { return contract.%sNotFound{Problem: scenery.Problem{Code: \"not_found\", Message: \"resource not found\"}}, nil }\n", operationName)
			case "invalid_cursor":
				fmt.Fprintf(&b, "\tif errors.Is(err, datasource.ErrCRUDInvalidCursor) { return contract.%sInvalidCursor{Problem: scenery.Problem{Code: \"SCN8001\", Message: \"cursor does not match the list query\"}}, nil }\n", operationName)
			}
		}
		b.WriteString("\tif err != nil { return nil, err }\n")
		fmt.Fprintf(&b, "\toutcome := contract.%s%s{}\n", operationName, goName(stringValue(result["name"])))
		fmt.Fprintf(&b, "\tif err := scenery.UnmarshalContractValue(result, &outcome.Value, %q); err != nil { return nil, err }\n", goWireTypeExpression(result["type"]))
		b.WriteString("\treturn outcome, nil\n}\n\n")
	}

	b.WriteString("func Register(registry scenery.Registry) error {\n")
	fmt.Fprintf(&b, "\treturn registry.Register(%q, sceneryruntime.ContractRegistration{\n", service.Address+"/adapter")
	fmt.Fprintf(&b, "\t\tContractRevision: ContractRevision, PackageContractABIRevision: PackageContractABIRevision, RuntimeABI: sceneryruntime.ContractRuntimeABI,\n\t\tProviderABIs: %s, CoveredAddresses: %#v,\n", goStringStringMap(providerABIs), covered)
	b.WriteString("\t\tApply: func() error {\n")
	fmt.Fprintf(&b, "\t\t\tif contract.PackageIdentity != PackageIdentity { return fmt.Errorf(\"package identity mismatch\") }\n")
	fmt.Fprintf(&b, "\t\t\tif contract.PackageContractABIRevision != PackageContractABIRevision { return fmt.Errorf(\"package contract ABI mismatch\") }\n")
	fmt.Fprintf(&b, "\t\t\tif err := sceneryruntime.RegisterNativeService(sceneryruntime.NativeServiceRegistration{Address: %q, Initialize: func(ctx context.Context) error { database, err := scenerydb.Get(ctx, %q); if err != nil { return err }; service = providerCRUDService{database: database}; return nil }}); err != nil { return err }\n", service.Address, databaseName)
	if err := renderDurableExecutionRegistrations(&b, service, providerOperations, resources); err != nil {
		return nil, err
	}
	if err := renderProviderCRUDInternalBindings(&b, providerOperations, resources); err != nil {
		return nil, err
	}
	if err := renderPageRegistrations(&b, resources, providerOperations); err != nil {
		return nil, err
	}
	for _, binding := range bindings {
		operation := operationForBinding(providerOperations, binding)
		if operation == nil {
			return nil, fmt.Errorf("provider CRUD binding %s references an unknown operation", binding.Address)
		}
		if err := renderHTTPBindingRegistration(&b, resources, service, *operation, binding); err != nil {
			return nil, err
		}
	}
	b.WriteString("\t\t\treturn nil\n\t\t},\n\t})\n}\n")
	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		return nil, fmt.Errorf("format provider CRUD adapter for %s: %w\n%s", service.Address, err, b.String())
	}
	_ = serviceName
	return formatted, nil
}

func providerCRUDOperations(operations []Resource) []Resource {
	result := make([]Resource, len(operations))
	for index, operation := range operations {
		result[index] = operation
		result[index].Spec = cloneMapValue(operation.Spec)
		handler := cloneMapValue(operation.Spec["handler"])
		if stringValue(handler["method"]) == "" {
			handler["method"] = goName(operation.Name)
		}
		result[index].Spec["handler"] = handler
	}
	return result
}

func operationHandlerMethod(operation Resource) string {
	handler, _ := operation.Spec["handler"].(map[string]any)
	method := stringValue(handler["method"])
	if method == "" && stringValue(handler["adapter"]) == "provider_crud_v1" {
		method = goName(operation.Name)
	}
	return method
}

func providerCRUDRuntimeSpec(resources []Resource, operations []Resource) (string, string, error) {
	byAddress := resourcesByAddress(&Manifest{Resources: resources})
	if len(operations) == 0 {
		return "", "", fmt.Errorf("provider CRUD service has no operations")
	}
	handler, _ := operations[0].Spec["handler"].(map[string]any)
	entity := byAddress[resolveResourceRef(operations[0], refString(handler["entity"]), "entity")]
	if entity.Kind != "scenery.entity" {
		return "", "", fmt.Errorf("provider CRUD operation has no entity")
	}
	record := byAddress[resolveResourceRef(entity, refString(entity.Spec["type"]), "record")]
	dataSource := byAddress[resolveResourceRef(entity, refString(entity.Spec["data_source"]), "data_source")]
	if record.Kind != "scenery.record" || dataSource.Kind != "scenery.data-source" {
		return "", "", fmt.Errorf("provider CRUD entity has no record or data source")
	}
	recordFields := map[string]map[string]any{}
	for _, field := range namedChildren(record.Spec, "field") {
		recordFields[stringValue(field["name"])] = field
	}
	mapping, _ := entity.Spec["mapping"].(map[string]any)
	relation := stringValue(mapping["relation"])
	schema := stringValue(mapping["schema"])
	var fields []string
	for _, field := range namedChildren(entity.Spec, "field") {
		name := stringValue(field["name"])
		recordField := recordFields[name]
		defaultSpec, _ := field["default"].(map[string]any)
		fields = append(fields, fmt.Sprintf("{Name: %q, Column: %q, Type: %q, PrimaryKey: %t, TenantKey: %t, Immutable: %t, DefaultStrategy: %q}", name, stringValue(field["column"]), goWireTypeExpression(recordField["type"]), field["primary_key"] == true, field["tenant_key"] == true, field["immutable"] == true, stringValue(defaultSpec["strategy"])))
	}
	config, _ := dataSource.Spec["config"].(map[string]any)
	databaseName := defaultString(stringValue(config["database"]), dataSource.Name)
	listLiteral := ""
	for _, operation := range operations {
		handler, _ := operation.Spec["handler"].(map[string]any)
		list, ok := handler["list"].(map[string]any)
		if !ok {
			continue
		}
		maxPageSize, _ := integerValue(list["max_page_size"])
		if maxPageSize == 0 {
			maxPageSize = 100
		}
		defaultSort, _ := list["default_sort"].(map[string]any)
		listLiteral = fmt.Sprintf(", List: &datasource.CRUDListSpec{Filters: %#v, Sorts: %#v, DefaultSort: %q, DefaultDirection: %q, MaxPageSize: %d}", stringValues(list["filters"]), stringValues(list["sorts"]), stringValue(defaultSort["field"]), stringValue(defaultSort["direction"]), maxPageSize)
		break
	}
	return fmt.Sprintf("datasource.CRUDSpec{Address: %q, Schema: %q, Relation: %q, Fields: []datasource.CRUDField{%s}%s}", entity.Address, schema, relation, strings.Join(fields, ", "), listLiteral), databaseName, nil
}

func renderProviderCRUDInternalBindings(b *strings.Builder, operations, resources []Resource) error {
	resourceMap := resourcesByAddress(&Manifest{Resources: resources})
	for _, binding := range internalBindingsForOperations(resources, operations) {
		operation := operationForBinding(operations, binding)
		if operation == nil {
			return fmt.Errorf("internal binding %s references an unknown provider CRUD operation", binding.Address)
		}
		method, operationName := operationHandlerMethod(*operation), goName(operation.Name)
		delivery := stringValue(binding.Spec["delivery"])
		internal, _ := binding.Spec["internal"].(map[string]any)
		visibility := stringValue(internal["visibility"])
		policy := renderContractInternalPolicy(resourceMap, binding)
		jsonCodecs := renderInternalBindingJSONCodecs(*operation, delivery)
		switch delivery {
		case "enqueue", "wait":
			execution, ok := executionForBinding(resourceMap, binding)
			if !ok || stringValue(execution.Spec["mode"]) != "durable" {
				return fmt.Errorf("%s binding %s does not select a durable execution", delivery, binding.Address)
			}
			if delivery == "enqueue" {
				fmt.Fprintf(b, "\t\t\tif err := sceneryruntime.RegisterContractInternalBindingWithPolicy(sceneryruntime.ContractInternalBindingRegistration{Address: %q, Visibility: %q, Package: %q, Policy: %s, %s Invoke: func(ctx context.Context, _ any, input any) (any, error) { typed, ok := input.(contract.%sInput); if !ok { return nil, sceneryruntime.ContractSystemError(fmt.Errorf(\"internal binding input has type %%T\", input)) }; copied, err := contract.Clone%sInput(typed); if err != nil { return nil, err }; options, err := %s(copied); if err != nil { return nil, err }; return sceneryruntime.DispatchContractDurableExecutionWithOptions(ctx, %q, copied, options) }}); err != nil { return err }\n", binding.Address, visibility, binding.Module, policy, jsonCodecs, operationName, operationName, durableDispatchOptionsFunction(execution), execution.Address)
			} else {
				fmt.Fprintf(b, "\t\t\tif err := sceneryruntime.RegisterContractInternalBindingWithPolicy(sceneryruntime.ContractInternalBindingRegistration{Address: %q, Visibility: %q, Package: %q, Policy: %s, %s Invoke: func(ctx context.Context, _ any, input any) (any, error) { typed, ok := input.(contract.%sInput); if !ok { return nil, sceneryruntime.ContractSystemError(fmt.Errorf(\"internal binding input has type %%T\", input)) }; copied, err := contract.Clone%sInput(typed); if err != nil { return nil, err }; options, err := %s(copied); if err != nil { return nil, err }; data, err := sceneryruntime.DispatchAndWaitContractDurableExecutionWithOptions(ctx, %q, copied, options); if err != nil { return nil, err }; return contract.Unmarshal%sOutcome(data) }}); err != nil { return err }\n", binding.Address, visibility, binding.Module, policy, jsonCodecs, operationName, operationName, durableDispatchOptionsFunction(execution), execution.Address, operationName)
			}
		default:
			fmt.Fprintf(b, "\t\t\tif err := sceneryruntime.RegisterContractInternalBindingWithPolicy(sceneryruntime.ContractInternalBindingRegistration{Address: %q, Visibility: %q, Package: %q, Policy: %s, %s Invoke: func(ctx context.Context, _ any, input any) (any, error) { if service == nil { return nil, sceneryruntime.ContractSystemError(fmt.Errorf(\"service is not initialized\")) }; typed, ok := input.(contract.%sInput); if !ok { return nil, sceneryruntime.ContractSystemError(fmt.Errorf(\"internal binding input has type %%T\", input)) }; copied, err := contract.Clone%sInput(typed); if err != nil { return nil, err }; outcome, err := service.%s(ctx, copied); if err != nil { return nil, err }; if outcome == nil { return nil, sceneryruntime.ContractSystemError(fmt.Errorf(\"handler returned nil outcome\")) }; cloned, err := contract.Clone%sOutcome(outcome); if err != nil { return nil, err }; return cloned, nil }}); err != nil { return err }\n", binding.Address, visibility, binding.Module, policy, jsonCodecs, operationName, operationName, method, operationName)
		}
	}
	return nil
}
