package generate

import (
	"bytes"
	"errors"
	"io/fs"
	"os"
	"path/filepath"
	"sort"

	"scenery.sh/internal/app"
	uicatalog "scenery.sh/ui"
)

// uiCatalogEntries mirrors the go:embed directive in ui/embed.go. A live
// catalog directory configured through envs.local.ui_catalog is walked with
// this exact entry set so repo files such as AGENTS.md or embed.go never
// materialize into clients.
var uiCatalogEntries = []string{"package.json", "global.d.ts", "index.ts", "tokens.stylex.ts", "components"}

func renderUICatalog(appRoot, root string) ([]generatedFile, error) {
	source, err := uiCatalogFS(appRoot)
	if err != nil {
		return nil, err
	}
	var files []generatedFile
	appendFile := func(path string) error {
		data, err := fs.ReadFile(source, path)
		if err != nil {
			return err
		}
		files = append(files, generatedFile{Path: filepath.Join(root, filepath.FromSlash(path)), Bytes: markGeneratedUICatalogFile(path, data)})
		return nil
	}
	for _, entry := range uiCatalogEntries {
		info, err := fs.Stat(source, entry)
		if errors.Is(err, fs.ErrNotExist) {
			continue
		}
		if err != nil {
			return nil, err
		}
		if !info.IsDir() {
			if err := appendFile(entry); err != nil {
				return nil, err
			}
			continue
		}
		err = fs.WalkDir(source, entry, func(path string, d fs.DirEntry, err error) error {
			if err != nil || d.IsDir() {
				return err
			}
			return appendFile(path)
		})
		if err != nil {
			return nil, err
		}
	}
	return files, nil
}

// includeStaleUICatalogFiles retires catalog files that disappeared from the
// live or embedded @scenery/ui source. Catalog sync may update files between
// full client generations, so descriptor integrity alone cannot identify these
// stale files reliably.
func includeStaleUICatalogFiles(result *Result, targets []Resource, files []generatedFile) ([]generatedFile, error) {
	expected := make(map[string]bool, len(files))
	for _, file := range files {
		expected[filepath.Clean(file.Path)] = true
	}
	var stale []string
	for _, target := range targets {
		if _, ok := target.Spec["react"].(map[string]any); !ok {
			continue
		}
		outputRoot, err := typeScriptOutputRoot(result, target)
		if err != nil {
			return nil, err
		}
		catalogRoot := filepath.Join(outputRoot, "react", "scenery-ui")
		err = filepath.WalkDir(catalogRoot, func(path string, entry os.DirEntry, walkErr error) error {
			if errors.Is(walkErr, fs.ErrNotExist) {
				return nil
			}
			if walkErr != nil {
				return walkErr
			}
			if entry.IsDir() {
				return nil
			}
			if entry.Type()&os.ModeSymlink != 0 {
				return errors.New("generated UI catalog contains a symlink: " + path)
			}
			path = filepath.Clean(path)
			if expected[path] {
				return nil
			}
			data, err := os.ReadFile(path)
			if err != nil {
				return err
			}
			relative, err := filepath.Rel(catalogRoot, path)
			if err != nil {
				return err
			}
			if trustedGeneratedArtifact("", filepath.ToSlash(relative), data) {
				stale = append(stale, path)
				expected[path] = true
			}
			return nil
		})
		if err != nil {
			return nil, err
		}
	}
	sort.Strings(stale)
	for _, path := range stale {
		files = append(files, generatedFile{Path: path, Remove: true})
	}
	return files, nil
}

// uiCatalogFS selects the catalog source: the live directory configured by
// the app's default environment (envs.local.ui_catalog), or the
// binary-embedded copy. Apps without a discoverable config, without the
// override, or whose configured directory is absent all use the embed; a
// present-but-implausible directory is a configuration error.
func uiCatalogFS(appRoot string) (fs.FS, error) {
	discoveredRoot, cfg, err := app.DiscoverRoot(appRoot)
	if err != nil {
		return uicatalog.Files, nil
	}
	env, err := cfg.ResolveEnv("")
	if err != nil {
		return uicatalog.Files, nil
	}
	dir, missing, err := env.UICatalogDir(discoveredRoot)
	if err != nil {
		return nil, err
	}
	if dir == "" || missing {
		return uicatalog.Files, nil
	}
	return os.DirFS(dir), nil
}

func markGeneratedUICatalogFile(path string, data []byte) []byte {
	const lineMarker = "// Code generated by Scenery. DO NOT EDIT.\n"
	switch filepath.Ext(path) {
	case ".json":
		return bytes.Replace(data, []byte("{"), []byte("{\n  \"_generated\": \"Code generated by Scenery. DO NOT EDIT.\","), 1)
	case ".css":
		return append([]byte("/* Code generated by Scenery. DO NOT EDIT. */\n"), data...)
	default:
		return append([]byte(lineMarker), data...)
	}
}
