package generate

import (
	"fmt"
	"sort"
	"strconv"
	"strings"
)

func goWireTypeExpression(value any) string {
	if reference := refString(value); reference != "" {
		return reference
	}
	if expression, ok := value.(map[string]any); ok {
		if raw, ok := expression["$expression"].(string); ok {
			return strings.TrimSpace(raw)
		}
	}
	return fmt.Sprint(value)
}

func renderGoContractConstraints(field map[string]any) string {
	var fields []string
	for _, key := range []string{"minimum", "maximum"} {
		if value := field[key]; value != nil {
			fields = append(fields, goName(key)+": scenery.ContractStringConstraint("+strconv.Quote(stringValue(value))+")")
		}
	}
	for _, key := range []string{"min_length", "max_length", "min_items", "max_items"} {
		if value := field[key]; value != nil {
			integer, _ := nonNegativeInteger(value)
			fields = append(fields, goName(key)+": scenery.ContractIntConstraint("+strconv.FormatInt(integer, 10)+")")
		}
	}
	for _, key := range []string{"pattern", "format"} {
		if value := stringValue(field[key]); value != "" {
			fields = append(fields, goName(key)+": "+strconv.Quote(value))
		}
	}
	if field["unique_items"] == true {
		fields = append(fields, "UniqueItems: true")
	}
	return "scenery.ContractConstraints{" + strings.Join(fields, ", ") + "}"
}

func renderContractAPI(packageIdentity, importPath, abi string, resources, allResources []Resource, typeResolver *goContractTypeResolver) (string, error) {
	dependencyImports := map[string]string{}
	serviceDependencies := map[string][]goDependencyBinding{}
	serviceClients := map[string][]goClientBinding{}
	hasInternalClients := false
	hasOperations := false
	for _, resource := range resources {
		hasOperations = hasOperations || resource.Kind == "scenery.operation"
	}
	for _, service := range resources {
		if service.Kind != "scenery.service" {
			continue
		}
		dependencies, err := serviceGoDependencies(allResources, service)
		if err != nil {
			return "", err
		}
		serviceDependencies[service.Address] = dependencies
		clients, err := serviceGoClients(allResources, service)
		if err != nil {
			return "", err
		}
		serviceClients[service.Address] = clients
		hasInternalClients = hasInternalClients || len(clients) > 0
		for _, dependency := range dependencies {
			dependencyImports[dependency.ImportAlias] = dependency.ImportPath
		}
		for _, client := range clients {
			if client.ContractAlias == "" {
				continue
			}
			if existing := dependencyImports[client.ContractAlias]; existing != "" && existing != client.ContractImport {
				return "", fmt.Errorf("generated import alias %s resolves to both %s and %s", client.ContractAlias, existing, client.ContractImport)
			}
			dependencyImports[client.ContractAlias] = client.ContractImport
		}
	}
	for _, value := range contractTypeValues(resources) {
		_ = typeResolver.Type(value)
	}
	for alias, importPath := range typeResolver.Imports() {
		if existing := dependencyImports[alias]; existing != "" && existing != importPath {
			return "", fmt.Errorf("generated import alias %s resolves to both %s and %s", alias, existing, importPath)
		}
		dependencyImports[alias] = importPath
	}
	var b strings.Builder
	b.WriteString("// Code generated by Scenery. DO NOT EDIT.\npackage scenerycontract\n\nimport (\n")
	if hasInternalClients {
		b.WriteString("\t\"context\"\n")
	}
	if hasOperations {
		b.WriteString("\t\"fmt\"\n")
	}
	b.WriteString("\tscenery \"scenery.sh\"\n")
	aliases := make([]string, 0, len(dependencyImports))
	for alias := range dependencyImports {
		aliases = append(aliases, alias)
	}
	sort.Strings(aliases)
	for _, alias := range aliases {
		fmt.Fprintf(&b, "\t%s %q\n", alias, dependencyImports[alias])
	}
	b.WriteString(")\n\n")
	fmt.Fprintf(&b, "const PackageIdentity = %q\nconst PackageContractABIRevision = %q\nconst PackageImportPath = %q\nconst GoImplementationABIRange = %q\nconst RuntimeABIRange = %q\n\n", packageIdentity, abi, importPath, ">=1.0.0, <2.0.0", "scenery.go-runtime/v1")
	for _, service := range resources {
		if service.Kind != "scenery.service" {
			continue
		}
		serviceName := goName(service.Name)
		for _, client := range serviceClients[service.Address] {
			operationName := goName(client.Operation.Name)
			contractQualifier := clientContractQualifier(client)
			fmt.Fprintf(&b, "type %s interface {\n", client.InterfaceName)
			if client.Delivery == "enqueue" {
				fmt.Fprintf(&b, "\tEnqueue(context.Context, scenery.Invocation, %s%sInput) (scenery.ExecutionReceipt, error)\n", contractQualifier, operationName)
			} else {
				fmt.Fprintf(&b, "\tInvoke(context.Context, scenery.Invocation, %s%sInput) (%s%sOutcome, error)\n", contractQualifier, operationName, contractQualifier, operationName)
			}
			b.WriteString("}\n\n")
		}
		fmt.Fprintf(&b, "type %sDependencies struct {\n", serviceName)
		for _, dependency := range serviceDependencies[service.Address] {
			fmt.Fprintf(&b, "\t%s %s\n", dependency.Field, dependency.GoType)
		}
		b.WriteString("}\n\n")
		fmt.Fprintf(&b, "type %sConfig struct {\n", serviceName)
		for _, field := range namedChildren(service.Spec, "config_schema") {
			fmt.Fprintf(&b, "\t%s %s\n", goName(stringValue(field["name"])), typeResolver.Type(field["type"]))
		}
		fmt.Fprintf(&b, "}\ntype %sClients struct {\n", serviceName)
		for _, client := range serviceClients[service.Address] {
			fmt.Fprintf(&b, "\t%s %s\n", client.Field, client.InterfaceName)
		}
		fmt.Fprintf(&b, "}\ntype %sConstructorInput struct {\n\tDependencies %sDependencies\n\tConfig %sConfig\n\tClients %sClients\n}\n\n", serviceName, serviceName, serviceName, serviceName)
	}
	for _, op := range resources {
		if op.Kind != "scenery.operation" {
			continue
		}
		renderOperationAPI(&b, op, typeResolver.Type)
	}
	return b.String(), nil
}

func renderOperationAPI(b *strings.Builder, op Resource, typeName func(any) string) {
	name := goName(op.Name)
	inputType := typeName(op.Spec["input"])
	if inputType != name+"Input" {
		fmt.Fprintf(b, "type %sInput = %s\n\n", name, inputType)
	}
	fmt.Fprintf(b, "func Unmarshal%sInput(data []byte) (%sInput, error) {\n", name, name)
	fmt.Fprintf(b, "\tvar value %sInput; if err := unmarshalGeneratedContractValue(data, &value, %q); err != nil { return value, err }; return value, nil\n}\n\n", name, goWireTypeExpression(op.Spec["input"]))
	fmt.Fprintf(b, "func Clone%sInput(value %sInput) (%sInput, error) {\n", name, name, name)
	fmt.Fprintf(b, "\traw, err := scenery.MarshalContractValue(value, %q); if err != nil { return value, err }\n", goWireTypeExpression(op.Spec["input"]))
	fmt.Fprintf(b, "\treturn Unmarshal%sInput(raw)\n}\n\n", name)
	fmt.Fprintf(b, "type %sOutcome interface { is%sOutcome() }\n\n", name, name)
	for _, kind := range []string{"result", "error"} {
		for _, variant := range namedChildren(op.Spec, kind) {
			variantName, _ := variant["name"].(string)
			wrapper := name + goName(variantName)
			valueType := typeName(variant["type"])
			field := "Value"
			if kind == "error" {
				field = "Problem"
			}
			fmt.Fprintf(b, "type %s struct { %s %s }\nfunc (%s) is%sOutcome() {}\n\n", wrapper, field, valueType, wrapper, name)
		}
	}
	fmt.Fprintf(b, "func Marshal%sOutcome(value %sOutcome) ([]byte, error) {\n\tswitch typed := value.(type) {\n", name, name)
	for _, kind := range []string{"result", "error"} {
		for _, variant := range namedChildren(op.Spec, kind) {
			variantName := stringValue(variant["name"])
			wrapper := name + goName(variantName)
			field := "Value"
			if kind == "error" {
				field = "Problem"
			}
			fmt.Fprintf(b, "\tcase %s: return scenery.MarshalContractOutcomeVariant(%q, %q, typed.%s, %q)\n", wrapper, kind, variantName, field, goWireTypeExpression(variant["type"]))
			fmt.Fprintf(b, "\tcase *%s: if typed == nil { return nil, fmt.Errorf(\"nil %s outcome\") }; return scenery.MarshalContractOutcomeVariant(%q, %q, typed.%s, %q)\n", wrapper, wrapper, kind, variantName, field, goWireTypeExpression(variant["type"]))
		}
	}
	b.WriteString("\tdefault: return nil, fmt.Errorf(\"unknown operation outcome %T\", value)\n\t}\n}\n\n")
	fmt.Fprintf(b, "func Unmarshal%sOutcome(data []byte) (%sOutcome, error) {\n", name, name)
	b.WriteString("\tkind, name, payload, err := scenery.DecodeContractOutcomeEnvelope(data); if err != nil { return nil, err }\n\tswitch {\n")
	for _, kind := range []string{"result", "error"} {
		for _, variant := range namedChildren(op.Spec, kind) {
			variantName := stringValue(variant["name"])
			wrapper := name + goName(variantName)
			valueType := typeName(variant["type"])
			field := "Value"
			if kind == "error" {
				field = "Problem"
			}
			fmt.Fprintf(b, "\tcase kind == %q && name == %q:\n\t\tvar value %s; if err := unmarshalGeneratedContractValue(payload, &value, %q); err != nil { return nil, err }; return %s{%s: value}, nil\n", kind, variantName, valueType, goWireTypeExpression(variant["type"]), wrapper, field)
		}
	}
	b.WriteString("\tdefault: return nil, fmt.Errorf(\"unknown operation outcome %s.%s\", kind, name)\n\t}\n}\n\n")
	fmt.Fprintf(b, "func Clone%sOutcome(value %sOutcome) (%sOutcome, error) {\n\traw, err := Marshal%sOutcome(value); if err != nil { return nil, err }; return Unmarshal%sOutcome(raw)\n}\n\n", name, name, name, name, name)
}

func namedChildren(spec map[string]any, name string) []map[string]any {
	children := orderedChildren(spec, name)
	sort.Slice(children, func(i, j int) bool {
		a, _ := children[i]["name"].(string)
		b, _ := children[j]["name"].(string)
		return a < b
	})
	return children
}

func orderedChildren(spec map[string]any, name string) []map[string]any {
	value, ok := spec[name]
	if !ok {
		return nil
	}
	switch typed := value.(type) {
	case map[string]any:
		return []map[string]any{typed}
	case []any:
		out := make([]map[string]any, 0, len(typed))
		for _, item := range typed {
			if child, ok := item.(map[string]any); ok {
				out = append(out, child)
			}
		}
		return out
	}
	return nil
}

func goType(value any) string {
	if ref := refString(value); ref != "" {
		for _, segment := range []string{"/record/", "/enum/", "/union/"} {
			if index := strings.LastIndex(ref, segment); index >= 0 {
				return goName(ref[index+len(segment):])
			}
		}
		parts := strings.Split(ref, ".")
		if len(parts) >= 2 {
			switch parts[0] {
			case "record", "enum", "union":
				return goName(parts[1])
			}
		}
		switch ref {
		case "std.type.problem":
			return "scenery.Problem"
		case "std.type.unit":
			return "scenery.Unit"
		case "string":
			return "string"
		case "bool":
			return "bool"
		case "int":
			return "scenery.Int"
		case "int32":
			return "int32"
		case "int64":
			return "int64"
		case "uint32":
			return "uint32"
		case "uint64":
			return "uint64"
		case "decimal":
			return "scenery.Decimal"
		case "float32":
			return "float32"
		case "float64":
			return "float64"
		case "bytes":
			return "[]byte"
		case "uuid":
			return "scenery.UUID"
		case "date":
			return "scenery.Date"
		case "datetime":
			return "scenery.DateTime"
		case "duration":
			return "scenery.Duration"
		case "size":
			return "scenery.Size"
		case "url":
			return "scenery.URL"
		case "relative_path":
			return "scenery.RelativePath"
		case "json":
			return "scenery.JSON"
		}
	}
	if expr, ok := value.(map[string]any); ok {
		if raw, ok := expr["$expression"].(string); ok {
			return goTypeExpression(raw)
		}
	}
	return "any"
}
