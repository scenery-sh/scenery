package uireport

import (
	"os"
	"path/filepath"
	"testing"

	appcfg "scenery.sh/internal/app"
)

func TestCollectFrontendsAndExclusions(t *testing.T) {
	root := t.TempDir()
	writeCollectFixture(t, root, "apps/react/src/page.tsx", `
import { Text } from "@astryxdesign/core/Text";
export const Page = () => <main><Text>Hello</Text></main>;
`)
	writeCollectFixture(t, root, "apps/react/src/page.test.tsx", `<div />`)
	writeCollectFixture(t, root, "apps/react/src/generated/client.tsx", `<div />`)
	writeCollectFixture(t, root, "apps/react/node_modules/pkg/index.tsx", `<div />`)
	writeCollectFixture(t, root, "apps/react/src/owned.tsx", `// Code generated by Scenery. DO NOT EDIT.
export const Owned = () => <div />;
`)
	writeCollectFixture(t, root, "apps/legacy/src/page.jsx", `export const Page = () => <div />;`)

	cfg := appcfg.Config{Frontends: map[string]appcfg.FrontendConfig{
		"react":  {Root: "apps/react"},
		"legacy": {Root: "apps/legacy"},
	}}
	report, err := Collect(root, cfg, "")
	if err != nil {
		t.Fatal(err)
	}
	if len(report.Frontends) != 2 || report.Frontends[0].Name != "legacy" || report.Frontends[1].Name != "react" {
		t.Fatalf("frontends = %#v", report.Frontends)
	}
	if report.Frontends[0].DesignSystem != "none" || len(report.Frontends[0].Files) != 0 {
		t.Fatalf("legacy report = %#v", report.Frontends[0])
	}
	react := report.Frontends[1]
	if react.DesignSystem != "astryx" || len(react.Files) != 1 || react.Files[0].Path != "src/page.tsx" {
		t.Fatalf("react report = %#v", react)
	}

	filtered, err := Collect(root, cfg, "react")
	if err != nil {
		t.Fatal(err)
	}
	if len(filtered.Frontends) != 1 || filtered.Frontends[0].Name != "react" {
		t.Fatalf("filtered report = %#v", filtered)
	}
	if _, err := Collect(root, cfg, "missing"); err == nil {
		t.Fatal("missing frontend filter succeeded")
	}
}

func TestCollectNoFrontends(t *testing.T) {
	report, err := Collect(t.TempDir(), appcfg.Config{}, "")
	if err != nil {
		t.Fatal(err)
	}
	if report.Frontends == nil || len(report.Frontends) != 0 {
		t.Fatalf("frontends = %#v, want non-nil empty slice", report.Frontends)
	}
}

func TestCollectRejectsEscapingFrontendRoot(t *testing.T) {
	root := t.TempDir()
	cfg := appcfg.Config{Frontends: map[string]appcfg.FrontendConfig{
		"bad": {Root: "../outside"},
	}}
	if _, err := Collect(root, cfg, ""); err == nil {
		t.Fatal("escaping root succeeded")
	}
}

func TestCollectRejectsFrontendSymlinkOutsideApp(t *testing.T) {
	root := t.TempDir()
	outside := t.TempDir()
	link := filepath.Join(root, "web")
	if err := os.Symlink(outside, link); err != nil {
		t.Skipf("symlinks unavailable: %v", err)
	}
	cfg := appcfg.Config{Frontends: map[string]appcfg.FrontendConfig{
		"web": {Root: "web"},
	}}
	if _, err := Collect(root, cfg, ""); err == nil {
		t.Fatal("outside symlink root succeeded")
	}
}

func writeCollectFixture(t *testing.T, root, relative, content string) {
	t.Helper()
	path := filepath.Join(root, filepath.FromSlash(relative))
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatal(err)
	}
}
