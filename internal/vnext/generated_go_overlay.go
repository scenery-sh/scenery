package vnext

import (
	"fmt"
	"go/parser"
	"go/token"
	"os"
	"path/filepath"
)

func goGeneratedDescriptorNames() map[string]bool {
	return map[string]bool{
		"scenery.package-generated.v1.json": true,
		"scenery.generated.v1.json":         true,
	}
}

// generatedGoVerificationOverlay stages expected artifacts and masks every
// descriptor-owned Go file scheduled for retirement. An empty overlay entry
// does not remove a file from go/packages, so removals use a package-only stub.
func generatedGoVerificationOverlay(files []generatedFile) (map[string][]byte, error) {
	overlay := map[string][]byte{}
	for _, file := range files {
		if filepath.Ext(file.Path) != ".go" {
			continue
		}
		absolute, err := filepath.Abs(file.Path)
		if err != nil {
			return nil, err
		}
		if !file.Remove {
			overlay[absolute] = file.Bytes
			continue
		}
		data, err := os.ReadFile(absolute)
		if os.IsNotExist(err) {
			continue
		}
		if err != nil {
			return nil, err
		}
		parsed, err := parser.ParseFile(token.NewFileSet(), absolute, data, parser.PackageClauseOnly)
		if err != nil {
			return nil, fmt.Errorf("parse stale generated Go package %s: %w", file.Path, err)
		}
		overlay[absolute] = []byte("// Code generated by Scenery vNext. DO NOT EDIT.\n\npackage " + parsed.Name.Name + "\n")
	}
	return overlay, nil
}
